package main

import "fmt"

func main() {
	angka1 := 10
	angka2 := 5

	fmt.Println("Penjumlahan = " , angka1 + angka2)
	fmt.Println("Pengurangan = " , angka1 - angka2)
	fmt.Println("Perkalian = " , angka1 * angka2)
	fmt.Println("Pembagian = " , angka1 / angka2)
	fmt.Println("Sisa Bagi = " , angka1 % angka2)
}
