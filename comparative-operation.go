package main

import "fmt"

func main() {
	// > < >= <= == !=
	angka1 := 10
	angka2 := 5

	fmt.Println("Angka1 > Angka2 = " , angka1 > angka2)
	fmt.Println("Angka1 >= Angka2 = " , angka1 >= angka2)
	fmt.Println("Angka1 < Angka2 = " , angka1 < angka2)
	fmt.Println("Angka1 <= Angka2 = " , angka1 <= angka2)
	fmt.Println("Angka1 == Angka2 = " , angka1 == angka2)
	fmt.Println("Angka1 != Angka2 = " , angka1 != angka2)
}
